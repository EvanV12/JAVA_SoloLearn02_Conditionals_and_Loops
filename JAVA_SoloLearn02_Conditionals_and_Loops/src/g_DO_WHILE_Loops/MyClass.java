package g_DO_WHILE_Loops;

public class MyClass {

	public static void main(String[] args) {
		
		int x = 1;
		
		do {
			System.out.println(x);
			x++;
		} while (x < 5);
		/*
		 1
		 2
		 3
		 4
		 */
		
		
		
		int y = 2;
		
		do {
			System.out.println(y);
			y++;
		} while (y < 0);
		// Outputs 1

	}

}
